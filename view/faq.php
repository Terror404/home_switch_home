				<h2 id="question1">Qu'est-ce qu'HomeSwitchHome?</h2>
				HomeSwitchHome est la plateforme idéale pour l'échange de maisons. Il vous suffit de vous créer un compte pour pouvoir poster sans attendre votre annonce de logement. Ensuite il ne vous reste plus qu'à contacter les membres dont les offres vous intéressent afin de leur faire une proposition d'échange. De même les membres conquis par votre logement pourront alors vous contacter.

				<h2 id="question2">Existe-t-il des règles pour échanger sa maison sur HomeSwitchHome?</h2>
				Il n'existe qu'une seule et unique règle pour l'échange de maisons : le respect.
				Nous comptons sur tous nos membres pour rendre la maison échangée dans son état initial. N'oubliez pas que lorsque vous êtes chez eux, eux sont chez vous!
				Jusqu'ici nous n'avons pas reçu de plaintes à ce sujet et espérons que cela perdurera.

				<h2 id="question3">Quels sont les avantages de l'échange de maisons?</h2>
				Le principal avantage de l'échange de maison est l'économie faite sur les frais de logement. En effet l'échange est 100% gratuit, tout comme l'inscription.
				Cela vous permet de dépenser cet argent autrement ou de faire des économies!

				<h2 id="question4">Je suis locataire. Puis-je également échanger mon logement?</h2>
				Tout à fait! L'échange de logement n'es pas uniquement réservé aux membres propriétaires, les locataires eux aussi sont les bienvenus!

				<h2 id="question5">Puis-je aussi échanger ma voiture?</h2>
				Oui, l'échange de voiture est possible. 
				De nombreuses annonces incluent la voiture dans l'échange. Cela permet de faire des économies conséquentes sur la location d'une voiture. Attention cependant à voir avec votre assureur les responsabilités, et de vous mettre d'accord avec la famille d'échange des modalités en cas d'accident.

				<h2 id="question6">Puis-je inscrire ma résidence secondaire?</h2>
				Bien sur, cela est possible. Vous avez le choix de ne proposer à l'échange que votre résidence secondaire, ou bien de proposer votre résidence principale ainsi que votre résidence secondaire. Cela vous permet en plus d'intéresser deux catégories différentes et ainsi d'augmenter le nombre de membres qui aimeraient échanger avec vous.

				<h2 id="question7">Nous sommes non-fumeurs, nous possédons un animal domestique. Pouvons-nous échanger?</h2>
				Tout à fait. Des mentions spéciales ont été prévues dans la description des annonces (fumeurs, animaux domestiques, etc) et vous pouvez effectuer une recherche en fonction de ces critères spéciques. Si vous avez une requête particulière, n'oubliez pas d'en discuter au préalable avec vos futurs partenaires.

				<h2 id="question8">Pourquoi ne puis-je pas envoyer de pièces jointes avec la messagerie?</h2>
				La messagerie a pour vocation de mettre en relation les utilisateurs. Pour les membres souhaitant s'envoyer des photos nous les invitons à s'échanger leurs adresses email respectives.
