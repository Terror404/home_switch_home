				<h2 id="question1">Comment s'inscrire, comment s'identifier ?</h2>
                                Pour s’inscrire, rien de plus simple, il vous suffit de cliquer sur le bouton « inscription » situé dans le coin haut-droit de chaque page du site et de remplir le formulaire d’inscription. Une fois terminée, vous pouvez vous identifier et accéder alors à toutes les fonctionnalités de notre site.
                                
				<h2 id="question2">Comment trouver un logement sur le site ?</h2>
				Vous pouvez rechercher une maison à partir de la page « recherche » (le lien se situe dans la barre de navigation). 
                                Là vous avez le choix entre une recherche rapide à l’aide de la carte interactive ou alors une recherche plus détaillé à partir du formulaire.
                                Il est également possible de visualiser les logements récemment ajoutés, les mieux notés, à partir de la page d’accueil du site.


				<h2 id="question3">Peut-on avoir des détails sur la maison que l'on a trouvé ?</h2>
				Vous pouvez visualiser de manière détaillée les informations d’une maison en consultant la « fiche maison » lui correspondant. 
                                On y accède à partir de la page de résultat de recherche ou de la page news en cliquant sur le bloc correspondant à la maison que vous souhaitez consulter.

				<h2 id="question4">Comment ajouter un de ses logement sur le site ?</h2>
				Pour ajouter une nouvelle maison, il suffit de cliquer sur « ajouter une maison » situé dans le menu déroulant « mon compte » de la barre de navigation ou alors sur le « + » situé dans le bloc en haut à droite de la page.
                                Après avoir complété le formulaire, votre maison sera ajoutée à notre base de donnée ce qui nous permettra de la proposer à d’autres utilisateurs en réponse à leurs recherche.


				<h2 id="question5">Comment créer une annonce en rapport avec un des logement ?</h2>
				Pour ajouter une nouvelle annonce, il faut se rendre sur la fiche maison et cliquer sur le bouton « ajouter une annonce ». Là il vous sera demandé de spécifier les dates pendant lesquelles vous souhaitez échanger votre maison ainsi que, si vous le souhaitez, des critères spécifique à cette annonce (donner à manger à des animaux, arroser des plantes, …)

				<h2 id="question6">Comment contacter les autres membres du site ?</h2>
				Afin de pouvoir contacter les autres membres du site facilement sans avoir à sortir de celui-ci, une messagerie interne au site vous est proposée.

				<h2 id="question7">Quelle est la procédure pour échanger une maison ?</h2>
				Pour échanger une maison avec un autre membre, vous n’êtes pas obligé de passer par le site. 
                                Cependant si vous voulez commenter et noter la personne que vous avez reçue chez vous ou la maison où vous vous êtes rendus, il faut remplir un formulaire d’échange qui devra être accepté par l’autre utilisateur. 
                                Lorsque vous vous trouvez sur la fiche d’une maison ou d’une annonce où vous voulez vous rendre, cliquez sur « proposer un échange ». Remplissez le formulaire. Lorsque vous aurez envoyé votre demande, il vous faudra attendre que l’autre utilisateur ait accepté celle-ci avant de pouvoir commenter ou noter. 
                                Vous pouvez suivre l’évolution de vos demandes à partir de l’onglet « mes échanges » dans la barre de navigation.

				<h2 id="question8">Comment commenter/noter un utilisateur ou une maison ?</h2> 
				Pour pouvoir commenter et noter une maison/utilisateur il faut qu’un échange ait eu lieu entre vous et un autre utilisateur. Une fois un échange finalisé « officiellement » sur le site, cliquez sur « mes échanges » situé dans le menu déroulant « mon compte » de la barre de navigation. Là, vous aurez accès à vos échanges en attente de notation ainsi que ceux que vous avez proposé à d’autres utilisateurs ainsi que ceux qui vous auront été adressé. Pour noter ou commenter un de ces échanges, cliquez sur commenté puis suivez les instructions du formulaire.
                                Votre commentaire et votre note seront posté sur le site dès que l’autre utilisateur aura fait de même.
                                
                                <h2 id='question9'>Comment gérer ses annonces, ses logements ?</h2>
                                La page « mes maisons » vous permet de visualiser l’ensemble des maisons que vous proposez à l’échange. A partir de cette page vous pouvez gérer vos maisons en rejoignant les fiches maisons correspondantes.
                                Vous pouvez gérer vos annonces à partir des fiches maisons auxquelles sont rattachées ces annonces. Vous pouvez gérer vos annonces en rejoignant les fiches annonces correspondantes.
                                
                                <h2 id='question10'>Peut-on changer la langue du site ?</h2>
                                Pour le moment, le site est traduit en Frnaçais et en Anglais. La langue par défaut est le Français mais vous pouvez changer celle-ci à partir des paramètres de votre profil.
                                
                                <h2 id='question11'>Peut-on désactiver l'alerte par mail ?</h2>
                                Le système d’alerte par mail nous permet de vous prévenir lorsqu’un évènement en rapport avec votre profil se produit (nouveau mail, échange,…)
                                Il est possible de désactiver cette fonctionnalité à partir des paramètres de votre profil.


